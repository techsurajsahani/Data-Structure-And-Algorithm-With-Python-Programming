# Bubble Sort 

def bubble_sort(a):
    print("Insertion Sort Algorithm :-")
    print("")
    n=len(a)
    for i in range(0,n):
        print("")
        print("Round ",i+1," :- ")
        print(a)
        for j in range(0,n-1-i):
            if a[j]>a[j+1]:
                a[j],a[j+1]=a[j+1],a[j]
            print(a)
                
a=[9,8,7,6,5,4,3,2,1]
bubble_sort(a)
print(a)

# Time Complexity :-
# Best Case :- O(n)
# Average Case :- O(n^2)
# Worst Case :- O(n^2)

# Space Complexity :- O(1)

